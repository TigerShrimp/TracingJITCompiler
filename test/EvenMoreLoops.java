public class EvenMoreLoops {
  public static void main(String[] args) {
    int a = 1;
    int i = 0, j = 0, k = 0, l = 0, m = 0, n = 0, o = 0, p = 0, q = 0;
    for (; i < 3; i++) {
      a += i;
    }
    for (; j < 3; j++) {
      a += i;
    }
    for (; k < 3; k++) {
      a += i;
    }
    for (; l < 3; l++) {
      a += i;
    }
    for (; m < 3; m++) {
      a += i;
    }
    for (; n < 3; n++) {
      a += i;
    }
    for (; o < 3; o++) {
      a += i;
    }
    for (; p < 3; p++) {
      a += i;
    }
    for (; q < 3; q++) {
      a += i;
    }
    System.out.println(a);
  }
}